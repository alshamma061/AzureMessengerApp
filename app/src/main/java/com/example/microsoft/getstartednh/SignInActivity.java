    private void authenticate() {
        // We first try to load a token cache if one exists.
       if (loadUserTokenCache(mClient))
        {
            Intent i = new Intent(SignInActivity.this, MainActivity.class);
            startActivity(i);
        }
        // If we failed to load a token cache, login and create a token cache
        else
        {
            // Login using the Facebook provider.
        ListenableFuture<MobileServiceUser> mLogin = mClient.login(MobileServiceAuthenticationProvider.Facebook);

        final long time1 = System.nanoTime();
            Futures.addCallback(mLogin, new FutureCallback<MobileServiceUser>() {//@@@@@@@@@@ #1 request @@@@@@@@@@

                @Override
                public void onSuccess(MobileServiceUser user) {
                    long time2 = System.nanoTime();
                    createAndShowDialog(String.format("You are now logged in - %1$2s", user.getUserId()), "Success");
                    long timeTaken = time2 - time1;
                    System.out.println( "********************************************************************");
                    System.out.println("Time taken when successfully sign-in " + timeTaken + " ns");
                    System.out.println("********************************************************************");
                    cacheUserToken(mClient.getCurrentUser());

                    Intent i = new Intent(SignInActivity.this, MainActivity.class);
                    startActivity(i);
                }
                @Override
                public void onFailure(Throwable exc) {
                    createAndShowDialog("You must log in. Login Required", "Error");
                }


            });
        }
    }

    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        // When request completes
        if (resultCode == RESULT_OK) {
            // Check the request code matches the one we send in the login request
            if (requestCode == FACEBOOK_LOGIN_REQUEST_CODE) {
                MobileServiceActivityResult result = mClient.onActivityResult(data);
                if (result.isLoggedIn()) {
                    // login succeeded
                    createAndShowDialog(String.format("You are now logged in - %1$2s", mClient.getCurrentUser().getUserId()), "Success");
                    Intent i = new Intent(SignInActivity.this, MainActivity.class);
                    startActivity(i);
                } else {
                    // login failed, check the error message
                    String errorMessage = result.getErrorMessage();
                    createAndShowDialog(errorMessage, "Error");
                }
            }
        }
    }

